function distancesByRowLabeled(
  rows,
  { oneBased = true, label = "indices" } = {}
) {
  const lastSeen = new Map(); // num -> last row index
  const firstSeen = new Map(); // num -> first row index
  const gaps = new Map(); // num -> [gaps]

  rows.forEach((row, i) => {
    const seenThisRow = new Set(); // ignore duplicates within the same row
    for (const num of row) {
      if (seenThisRow.has(num)) continue;
      seenThisRow.add(num);

      if (!firstSeen.has(num)) firstSeen.set(num, i);

      if (lastSeen.has(num)) {
        const gap = i - lastSeen.get(num); // row distance
        (gaps.get(num) ?? gaps.set(num, []).get(num)).push(gap);
      }
      lastSeen.set(num, i);
    }
  });

  // Build { "<num>_<label><firstPos>": [gaps] } only for nums with gaps
  const entries = [...gaps.entries()]
    .sort((a, b) => a[0] - b[0]) // sort by number
    .map(([num, arr]) => {
      const firstPos = oneBased ? firstSeen.get(num) + 1 : firstSeen.get(num);
      const key = `${num}_${label}${firstPos}`;
      return [key, arr];
    });

  return Object.fromEntries(entries);
}

