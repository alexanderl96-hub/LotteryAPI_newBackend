function mulberry32(seed) {
  return function () {
    let t = (seed += 0x6d2b79f5);
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

// Sample k unique ints from [1..maxN] using partial Fisher–Yates (no full array copies)
function sampleKofN(k = 5, maxN = 69, rng = Math.random) {
  const a = Array.from({ length: maxN }, (_, i) => i + 1);
  for (let i = 0; i < k; i++) {
    const j = i + Math.floor(rng() * (maxN - i));
    [a[i], a[j]] = [a[j], a[i]];
  }
  const out = a.slice(0, k);
  out.sort((x, y) => x - y);
  return out;
}

