      function buildSeedIndexMap(targetObj, { oneBased = false } = {}) {
  const re = /^(\d+)_indices(\d+)$/; // "122020_indices10"
  const map = new Map();

  for (const [key, val] of Object.entries(targetObj)) {
    const m = re.exec(key);
    if (!m) continue;
    const seed = Number(m[1]);
    const group = Number(m[2]);

    const rawList = Array.isArray(val) ? val : [val];
    // normalize -> 0-based integers
    const indices = [...new Set(rawList // dedupe
      .map(n => (typeof n === "number" ? n : NaN))
      .filter(Number.isFinite)
      .map(n => oneBased ? n - 1 : n)
      .filter(n => Number.isInteger(n) && n >= 0)
    )].sort((a, b) => a - b);

    if (!indices.length) continue;

    if (!map.has(seed)) map.set(seed, []);
    map.get(seed).push({ group, indices });
  }
  return map;
}

      function findRowsByCompositeTarget(simulate, targetObj, { oneBased = false, withMeta = true } = {}) {
  const seedMap = buildSeedIndexMap(targetObj, { oneBased });
  const out = [];

  for (const item of simulate) {
    const { rows, seed } = unwrapItem(item);
    const entries = seedMap.get(Number(seed));
    if (!entries) continue;

    for (const { group, indices } of entries) {
      for (const idx of indices) {
        if (idx >= 0 && idx < rows.length) {
          const row = rows[idx];
          out.push(withMeta ? { seed, group, index: idx, row } : row);
        }
      }
    }
  }
  return out;
}

